import mongoose from "mongoose";
import { IAddEmployee } from "../interface/Employee";

const addEmployeeSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const NewEmployee = mongoose.model<IAddEmployee>(
  "AddEmployee",
  addEmployeeSchema
);

export default NewEmployee;
